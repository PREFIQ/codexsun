import { loadEnv } from "@codexsun/framework/env";
import { z } from "zod";

const envSchema = z.object({
  NODE_ENV: z.enum(["development", "test", "staging", "production"]).default("development"),
  AUTH_MODE: z.enum(["cookie", "jwt", "hybrid"]).default("jwt"),
  PLATFORM_API_HOST: z.string().default("127.0.0.1"),
  PLATFORM_API_PORT: z.coerce.number().int().positive(),
  PLATFORM_WEB_ORIGIN: z.string().min(1, "PLATFORM_WEB_ORIGIN is required"),
  DB_HOST: z.string().default("127.0.0.1"),
  DB_PORT: z.coerce.number().int().positive(),
  DB_USER: z.string().min(1, "DB_USER is required"),
  DB_PASSWORD: z.string(),
  DB_DRIVER: z.enum(["mariadb", "mysql2"]).default("mariadb"),
  DB_MASTER_NAME: z.string().min(1, "DB_MASTER_NAME is required"),
  CODEXSUN_DB_FRESH_ON_START: z.enum(["0", "1"]).default("0"),
  CODEXSUN_DB_RESET_CONFIRM: z.string().default(""),
  CODEXSUN_ALLOW_PRODUCTION_DB_RESET: z.enum(["0", "1"]).default("0"),
  CODEXSUN_BACKUP_DIR: z.string().default("storage/backups/database"),
  CODEXSUN_QUEUE_BACKEND: z.enum(["database", "bullmq-redis"]).default("database"),
  CODEXSUN_QUEUE_COMPLETED_RETENTION_DAYS: z.coerce.number().int().positive().default(14),
  CODEXSUN_QUEUE_FAILED_RETENTION_DAYS: z.coerce.number().int().positive().default(30),
  CODEXSUN_QUEUE_WORKER_ENABLED: z.enum(["0", "1"]).default("1"),
  CODEXSUN_QUEUE_WORKER_INTERVAL_MS: z.coerce.number().int().positive().default(5000),
  CODEXSUN_REDIS_URL: z.string().min(1, "CODEXSUN_REDIS_URL is required"),
  CODEXSUN_ALLOW_LIVE_RESTORE: z.enum(["0", "1"]).default("0"),
  CODEXSUN_LIVE_RESTORE_CONFIRM: z.string().default(""),
  CODEXSUN_RESTORE_TEST_DB_NAME: z.string().default(""),
  CODEXSUN_VERIFIED_BACKUP_ID: z.string().default(""),
  ENABLE_DEFAULT_TENANT_SEED: z.enum(["0", "1"]).default("0"),
  DEV_AUTO_TENANT_LOGIN: z.enum(["0", "1"]).default("0"),
  TENANT_DOMAIN_BASE: z.string().default("localhost"),
  DEFAULT_TENANT_ADMIN_EMAIL: z.string().default(""),
  DEFAULT_TENANT_ADMIN_NAME: z.string().default(""),
  DEFAULT_TENANT_ADMIN_PASSWORD: z.string().default(""),
  DEFAULT_TENANT_CORPORATE_ID: z.string().default(""),
  DEFAULT_TENANT_DB_NAME: z.string().default(""),
  DEFAULT_TENANT_DOMAIN: z.string().default("localhost"),
  DEFAULT_TENANT_NAME: z.string().default(""),
  DEFAULT_TENANT_SLUG: z.string().default(""),
  JWT_SECRET: z.string().min(1, "JWT_SECRET is required"),
  SOFTWARE_ADMIN_EMAIL: z.string().default(""),
  SOFTWARE_ADMIN_NAME: z.string().default(""),
  SOFTWARE_ADMIN_PASSWORD: z.string().default(""),
  SUPER_ADMIN_EMAIL: z.string().default(""),
  SUPER_ADMIN_NAME: z.string().default(""),
  SUPER_ADMIN_PASSWORD: z.string().default(""),
  TENANT_ADMIN_EMAIL: z.string().default(""),
  TENANT_ADMIN_NAME: z.string().default(""),
  TENANT_ADMIN_PASSWORD: z.string().default(""),
  STORAGE_PUBLIC_ROOT: z.string().default("storage/public"),
  STORAGE_ROOT: z.string().default("storage/app")
});

export const env = loadEnv(envSchema);
